/**
 * Generates the client from a schema file path.
 *
 * @param schemaPath Path to the .practor schema file
 * @param outputDir Output directory for generated TypeScript
 */
export async function generateFromSchema(
  schemaPath: string,
  outputDir: string,
): Promise<void> {
  const { execFileSync } = await import("child_process");

  // Use the Go engine to parse the schema
  const enginePath =
    process.env.PRACTOR_ENGINE_PATH ||
    path.resolve(process.cwd(), "node_modules", ".practor", "practor-engine");

  let schemaJSON: string;
  try {
    schemaJSON = execFileSync(enginePath, ["parse", schemaPath], {
      encoding: "utf-8",
      timeout: 10_000,
    });
  } catch (err: any) {
    // Fallback: try the engine from bin directory
    const binPath = path.resolve(process.cwd(), "bin", "practor-engine");
    schemaJSON = execFileSync(binPath, ["parse", schemaPath], {
      encoding: "utf-8",
      timeout: 10_000,
    });
  }

  const schema = JSON.parse(schemaJSON) as Schema;
  generate(schema, outputDir);
}
