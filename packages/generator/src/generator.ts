/**
 * @practor/generator — TypeScript Client Code Generator
 *
 * Reads a parsed Practor schema (JSON from the Go engine) and generates
 * a type-safe TypeScript client with:
 * - Model types (User, Post, etc.)
 * - Input types (UserCreateInput, UserWhereInput, etc.)
 * - Client class with model delegates
 * - Enum types
 */

import * as fs from "fs";
import * as path from "path";

// ============================================================================
// Schema types (matching Go AST JSON output)
// ============================================================================

interface Schema {
  datasources: Datasource[];
  generators: Generator[];
  models: Model[];
  enums: SchemaEnum[];
}

interface Datasource {
  name: string;
  provider: string;
  url: string;
  isEnvVar: boolean;
  envVarName?: string;
}

interface Generator {
  name: string;
  provider: string;
  output?: string;
}

interface Model {
  name: string;
  fields: Field[];
  dbName?: string;
}

interface Field {
  name: string;
  type: FieldType;
  isList: boolean;
  isOptional: boolean;
  attributes?: FieldAttribute[];
  defaultValue?: DefaultValue;
}

interface FieldType {
  name: string;
  isScalar: boolean;
  isEnum: boolean;
  isModel: boolean;
}

interface FieldAttribute {
  name: string;
  args?: Record<string, unknown>;
}

interface DefaultValue {
  type: number; // 0 = literal, 1 = function, 2 = enum
  value?: unknown;
  funcName?: string;
}

interface SchemaEnum {
  name: string;
  values: { name: string }[];
}

// ============================================================================
// Type mapping
// ============================================================================

const SCALAR_TO_TS: Record<string, string> = {
  String: "string",
  Int: "number",
  Float: "number",
  Boolean: "boolean",
  DateTime: "Date",
  Json: "JsonValue",
  BigInt: "bigint",
  Bytes: "Buffer",
  Decimal: "string", // Prisma-compatible: Decimal as string
};

/** Maps a Practor field type to a TypeScript type string. */
function fieldToTsType(field: Field, enums: Set<string>): string {
  if (field.type.isScalar) {
    return SCALAR_TO_TS[field.type.name] ?? "unknown";
  }
  if (field.type.isEnum || enums.has(field.type.name)) {
    return field.type.name;
  }
  if (field.type.isModel) {
    return field.type.name;
  }
  // Fallback: could be enum or model not yet resolved
  if (enums.has(field.type.name)) {
    return field.type.name;
  }
  return SCALAR_TO_TS[field.type.name] ?? field.type.name;
}

/** Checks if a field has the given attribute. */
function hasAttribute(field: Field, name: string): boolean {
  return field.attributes?.some((a) => a.name === name) ?? false;
}

/** Checks if a field is auto-generated (autoincrement, now, uuid, cuid, updatedAt). */
function isAutoGenerated(field: Field): boolean {
  if (hasAttribute(field, "updatedAt")) return true;
  if (field.defaultValue) {
    if (field.defaultValue.type === 1) {
      const fn = field.defaultValue.funcName;
      return (
        fn === "autoincrement" || fn === "now" || fn === "uuid" || fn === "cuid"
      );
    }
  }
  return false;
}

